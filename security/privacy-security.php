        <div class="privacy-card">
            <h2>Our Data Safety Principles</h2>

            <ul class="security-list">
                <li>Do not store information that is not needed.</li>
                <li>Avoid collecting sensitive student details unless there is a clear and necessary reason.</li>
                <li>Use fake or test data while developing and testing the system.</li>
                <li>Protect user passwords using secure password hashing.</li>
                <li>Use HTTPS to protect information sent between the user and the website.</li>
                <li>Keep regular backups to reduce the risk of data loss.</li>
                <li>Limit admin access to authorised users only.</li>
                <li>Delete old or unnecessary data when it is no longer needed.</li>
                <li>Protect important backups using encryption where appropriate.</li>
            </ul>
        </div>

        <div class="privacy-card">
            <h2>Protecting Learners</h2>

            <p>
                DM Studio AI is designed with learners in mind. Because the platform may be used
                by students who need additional support, it is especially important that information
                is handled carefully.
            </p>

            <p>
                The system should not collect medical details, diagnosis information, home addresses,
                private family information, or unnecessary sensitive information unless this is absolutely
                necessary and properly authorised.
            </p>
        </div>

        <div class="privacy-card">
            <h2>Responsible Development</h2>

            <p>
                During development, the system should use test accounts and example data instead of
                real student information. This helps protect privacy while allowing the platform to be
                tested and improved safely.
            </p>
        </div>

        <div class="privacy-card">
            <h2>Password Protection</h2>

            <p>
                User passwords should never be stored as plain text. Passwords should be protected
                using secure password hashing, so that the real password is not visible inside the database.
            </p>
        </div>

        <div class="privacy-card">
            <h2>Access Control</h2>

            <p>
                Different users have different levels of access. Students, teachers, managers, and the
                system owner should only see the information they need for their role.
            </p>

            <p>
                This helps prevent unauthorised access and keeps the system safer.
            </p>
        </div>

        <div class="privacy-card">
            <h2>Backups and Recovery</h2>

            <p>
                Regular backups help protect the platform from data loss. If there is a technical problem,
                mistake, or server issue, backups can help restore the system.
            </p>

            <p>
                Important backups should be protected carefully and encrypted where appropriate.
            </p>
        </div>

        <div class="privacy-card privacy-highlight">
            <h2>Our Commitment</h2>

            <p>
                DM Studio AI follows a simple privacy rule:
                <strong>collect less, protect more.</strong>
            </p>

            <p>
                By reducing unnecessary data collection and using careful security practices,
                the platform aims to create a safer digital learning environment for students and staff.
            </p>
