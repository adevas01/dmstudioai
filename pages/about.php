        DM Studio AI is an MSc Computing final project exploring how artificial intelligence
        can support Digital Media students, especially SEND learners.
    </p>
</section>

<section class="about-section">
    <div class="about-text">
        <h2>Project Aim</h2>
        <p>
            The aim of this project is to design and develop an accessible AI-supported learning
            platform that helps students understand digital media tasks more clearly, work more
            independently, and build confidence through step-by-step support.
        </p>

        <h2>Why this project matters</h2>
        <p>
            Digital media students often need support with planning, following instructions,
            organising coursework, reviewing their work, and understanding creative tasks.
            DM Studio AI explores how AI can make this process easier, more visual, and more inclusive.
        </p>

        <h2>Key Focus Areas</h2>
        <ul>
            <li>Accessible learning design</li>
            <li>SEND-friendly instructions</li>
            <li>AI-assisted project support</li>
            <li>Digital media skills development</li>
            <li>Teacher and student dashboards</li>
        </ul>
    </div>
</section>